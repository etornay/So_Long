#include "libft.h"

int	ft_putstr(char *s)
{
	int	x;

	x = 0;
	if (s == NULL)
		s = "(null)";
	while (s[x] != '\0')
	{
		write (1, &s[x], 1);
		x++;
	}
	return (x);
}
