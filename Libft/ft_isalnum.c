#include "libft.h"

int	ft_isalnum(int x)
{
	if ((x >= 'A' && x <= 'Z') || (x >= 'a' && x <= 'z')
		|| (x >= '0' && x <= '9'))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
